package com.company;

public class Dog {

    String barking;
    boolean howling;
    int whining;


    public static String bark(String barking){
        barking = barking;
        return barking;
    }

    public static boolean bark(boolean howling){
        howling = howling;
        return howling;
    }

    public static int bark(int whining){
        whining = whining;
        return whining;
    }
}
